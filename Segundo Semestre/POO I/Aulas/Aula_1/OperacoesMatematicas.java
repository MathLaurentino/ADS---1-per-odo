package Aula_1;

public class OperacoesMatematicas {
	
	public int somar(int num1, int num2) {
		int soma = num1 + num2;
		return soma;
	}
	
	public int subtrair(int num1, int num2) {
		int soma = num1 - num2;
		return soma;
	}

	public int dividir(int num1, int num2) {
		int soma = num1 / num2;
		return soma;
	}
	
	public int multiplicar(int num1, int num2) {
		int soma = num1 * num2;
		return soma;
	}
	
}
