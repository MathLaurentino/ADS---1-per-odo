<?php

    echo "While: <br>";
    $x = 1;
    while ($x <= 10) {
        echo $x . "<br>";
        $x++;
    };


    echo "<br> Do While: <br>";
    $y = 1;
    do {
        echo $y . "<br>";
        $y++;
    } while($y <= 10);


    echo "<br> For: <br>";
    for ($z=1; $z <= 10; $z++) {
        echo $z . "<br>";   
    }